x = 5
if x == 5:
    print("x is five")
if x == 5 and x > 4:
    print("x is greater than four")
if x == 5 and x < 6:
    print("x is less than six")
if x == 5 and x <= 5:
    print("x is less than or equal to five")
if x == 5 and x >= 5:
    print("x is greater than or equal to five")
if x == 5 and x != 6:
    print("x is not six")